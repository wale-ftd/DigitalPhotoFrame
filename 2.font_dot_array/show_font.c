#define FONTDATAMAX 4096
#define FB_PATHNAME "/dev/fb0"
#define HZK_PATHNAME "HZK16"
#define LCD_COLOR_BLACK 0x0
#define LCD_COLOR_WHITE 0xff
#define ASCIIDATA_X_MAX 8
#define ASCIIDATA_Y_MAX 16
#define HZDATA_X_MAX 16
#define HZDATA_Y_MAX 16
#define HZ_AREA 0xa1
#define HZ_FIELD 0xa1
#define HZ_NUM_PER_AREA 94
#define BITS_PER_BYTE 8

static const unsigned char fontdata_8x16[FONTDATAMAX];
static void show_font_ascii(int x, int y, unsigned char c);
static void show_font_chinese(int x, int y, unsigned char *s);
static void lcd_put_pixel(int x, int y, unsigned int color);

unsigned char *fb_mem;
struct fb_var_screeninfo fb_var;    /* Current var */
int fb_pixel_byte = 0;
unsigned char *hzk_mem;

int main(int argc, char **argv)
{
    int fb_fd;
	struct fb_fix_screeninfo fb_fix;	/* Current fix */
    int fb_mem_size = 0;
    int hzk_fd;
    struct stat stat_buf;
    unsigned char ascii = 'A';
    unsigned char str[] = "中";

    fb_fd = open(FB_PATHNAME, O_RDWR);
    if(-1 == fb_fd)
    {
        printf("%s can't be opened.\n", FB_PATHNAME);
        return -1;
    }

    if(ioctl(fb_fd, FBIOGET_VSCREENINFO, &fb_var))
    {
        printf("can't get fb_var_screeninfo.\n");
        return -1;
    }
    if(ioctl(fb_fd, FBIOGET_FSCREENINFO, &fb_fix))
    {
        printf("can't get fb_fix_screeninfo.\n");
        return -1;
    }
    printf("xres = %d, yres = %d, bpp = %d.\n", fb_var.xres, fb_var.yres, fb_var.bits_per_pixel);

    fb_pixel_byte = fb_var.bits_per_pixel / 8;
    fb_mem_size = fb_var.xres * fb_var.yres * fb_pixel_byte;
    
    fb_mem = (unsigned char *)mmap(NULL, fb_mem_size, PROT_READ | PROT_WRITE, MAP_SHARED, fb_fd, 0);
    if((unsigned char *)(-1) == fb_mem)
    {
        printf("fb device can't be mmapped.\n");
        return -1;
    }

    hzk_fd = open(HZK_PATHNAME, O_RDONLY);
    if(-1 == hzk_fd)
    {
        printf("%s can't be opened.\n", HZK_PATHNAME);
        return -1;
    }
    
    if(-1 == fstat(hzk_fd, &stat_buf))
    {
        printf("HZK16 fstat fail.\n");
        return -1;
    }
    
    hzk_mem = (unsigned char *)mmap(NULL, stat_buf.st_size, PROT_READ, MAP_SHARED, hzk_fd, 0);
    if((unsigned char *)(-1) == hzk_mem)
    {
        printf("HZK16 can't be mmapped.\n");
        return -1;
    }

	/* 清屏: 设为白色背景 */
    memset(fb_mem, LCD_COLOR_WHITE, fb_mem_size);

    show_font_ascii(fb_var.xres/2, fb_var.yres/2, ascii);

    show_font_chinese(fb_var.xres/2+ASCIIDATA_X_MAX, fb_var.yres/2, str);
    printf("chinese code: %02x, %02x.\n", str[0], str[1]);

    return 0;
}

static void show_font_ascii(int x, int y, unsigned char c)
{
    static int bytes_per_asciidata = ASCIIDATA_X_MAX * ASCIIDATA_Y_MAX / 8;
    unsigned char *asciidata_base = ((unsigned char *)fontdata_8x16) + c * bytes_per_asciidata;
    int b, asciidata_y;
    unsigned char byte;
    
    for(asciidata_y = 0; ASCIIDATA_Y_MAX > asciidata_y; asciidata_y++)
    {
        byte = asciidata_base[asciidata_y];
        
        for(b = BITS_PER_BYTE-1; 0 <= b; b--)
        {
            if((byte>>b) & 0x01)
            {
                lcd_put_pixel(x+(BITS_PER_BYTE-1)-b, y+asciidata_y, 0);             /* 黑 */
            }
            else
            {
                lcd_put_pixel(x+(BITS_PER_BYTE-1)-b, y+asciidata_y, 0xffffffff);    /* 白 */
            }
        }
    }
}

static void show_font_chinese(int x, int y, unsigned char *s)
{
    static int bytes_per_hzdata = HZDATA_X_MAX * HZDATA_Y_MAX / 8;
    static int bytes_per_x_hzdata = HZDATA_X_MAX / 8;
    int area = s[0] - HZ_AREA;
    int field = s[1] - HZ_FIELD;
    unsigned char *hzdata_base = hzk_mem + (area*HZ_NUM_PER_AREA+field) * bytes_per_hzdata;
    int hzdata_x, hzdata_y, b;
    unsigned char byte;

    for(hzdata_y = 0; HZDATA_Y_MAX > hzdata_y; hzdata_y++)
    {
#if 1
        for(hzdata_x = 0; bytes_per_x_hzdata > hzdata_x; hzdata_x++)
        {
            byte = hzdata_base[hzdata_y * bytes_per_x_hzdata + hzdata_x];

            for(b = BITS_PER_BYTE-1; 0 <= b; b--)
            {
                if((byte>>b) & 0x01)
                {
                    lcd_put_pixel(x+hzdata_x*BITS_PER_BYTE+(BITS_PER_BYTE-1)-b, y+hzdata_y, 0);             /* 黑 */
                }
                else
                {
                    lcd_put_pixel(x+hzdata_x*BITS_PER_BYTE+(BITS_PER_BYTE-1)-b, y+hzdata_y, 0xffffffff);    /* 白 */
                }
            }
        }
#else
        for(hzdata_x = HZDATA_X_MAX-1; 0 <= hzdata_x; hzdata_x--)
        {
            if(hzdata_x >= 8)
            {
                if(hzdata_base[hzdata_y*bytes_per_x_hzdata]>>(hzdata_x-8) & 0x01)
                {
                    /* 不是y+hzdata_y*bytes_per_x_hzdata，而是y+hzdata_y。 */
                    //lcd_put_pixel(x+HZDATA_X_MAX-1-hzdata_x, y+hzdata_y*bytes_per_x_hzdata, 0);             /* 黑 */
                    lcd_put_pixel(x+HZDATA_X_MAX-1-hzdata_x, y+hzdata_y, 0);             /* 黑 */
                }
                else
                {
                    lcd_put_pixel(x+HZDATA_X_MAX-1-hzdata_x, y+hzdata_y, 0xffffffff);    /* 白 */
                }
            }
            else
            {
                if(hzdata_base[hzdata_y*bytes_per_x_hzdata+1]>>(hzdata_x) & 0x01)
                {
                    lcd_put_pixel(x+HZDATA_X_MAX-1-hzdata_x, y+hzdata_y, 0);             /* 黑 */
                }
                else
                {
                    lcd_put_pixel(x+HZDATA_X_MAX-1-hzdata_x, y+hzdata_y, 0xffffffff);    /* 白 */
                }
            }
        }
#endif
    }
}

/* color : 0x00RRGGBB */
static void lcd_put_pixel(int x, int y, unsigned int color)
{
    unsigned char *pen8   = fb_mem + (x+y*fb_var.xres) * fb_pixel_byte;
    unsigned short *pen16 = (unsigned short *)pen8;
    unsigned int *pen32   = (unsigned int *)pen8;

    switch(fb_var.bits_per_pixel)
    {
        case 8:
        {
            *pen8 = (unsigned char)color;
            
            break;
        }
        case 16:    /* RGB: 565 */
        {
            unsigned int r, g, b;
            
            r = color >> 16 & 0xff;
            g = color >> 8 & 0xff;
            b = color & 0xff;
            color = ((r>>3)<<11) | ((g>>2)<<5) | (b>>3);

            *pen16 = (unsigned short)color;
            
            break;
        }
        case 32:
        {
            *pen32 = color;
            
            break;
        }
        default:
        {
            printf("%dbpp can't be supported.", fb_var.bits_per_pixel);
            
            break;
        }
    }
}

